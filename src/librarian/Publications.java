    public void setData(){
        
        try {
            Statement s = ConnectThanh.Connection().createStatement();
            ResultSet rs = s.executeQuery("select * from AnPham where maAP = '" + maap + "';");
            while(rs.next()){
                lblMaAP.setText(rs.getString("maAP"));
                lblTenAP.setText(rs.getString("tenAP"));
                lblTheloai.setText(rs.getString("Theloai"));
                lblTacgia.setText(rs.getString("Tacgia"));
                lblSoluong.setText(rs.getString("soluong"));
                lblPhuthu.setText(rs.getString("Phuthu"));
                lblConlai.setText(rs.getString("hienco"));
                path = rs.getString("pathing");
                ImageIcon icon = Edit_Img.Edit_Img(path, lblImage);
                lblImage.setIcon(icon);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }

