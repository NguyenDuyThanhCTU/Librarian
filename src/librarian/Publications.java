    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
   
        int input = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa ấn phẩm này", "THÔNG BÁO", JOptionPane.YES_NO_OPTION);
        if(input == 0){
            try {
                Statement s = ConnectThanh.Connection().createStatement();
                s.execute("delete from AnPham where maAP = " + Integer.parseInt(lblMaAP.getText()));
                JOptionPane.showMessageDialog(this, "Đã xóa thành công ", "THÔNG BÁO", JOptionPane.DEFAULT_OPTION);
                
                ConnectThanh.Connection().close();
            }catch(Exception e){
                e.printStackTrace();
            }
        } 
    }//GEN-LAST:event_btnDeleteActionPerformed

